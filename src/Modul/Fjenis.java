    public void getDataKendaraan(){
        //kosongkan tabel
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        //eksekusi koneksi dan kirimkan query ke database
        try{
            //tes koneksi
            Statement stat = (Statement) koneksiDB.getKoneksi().createStatement();
            
            //perintah sql untuk membaca data dari tabel kategori        
            String sql = "SELECT * FROM jenis_kendaraan";
            ResultSet res = stat.executeQuery(sql);
            
            //baca data
            while(res.next()){
                //membuat obyek berjenis array
                Object[] obj = new Object[3];
                obj[0]=res.getString("kd_kendaraan");
                obj[1]=res.getString("nm_kendaraan");
                obj[2]=res.getString("tarif");
                model.addRow(obj);
            }
        }catch(SQLException err){
           JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
    public void loadDataKendaraan(){
        kd_kendaraan = kd_kenda.getText();
        nm_kendaraan = nm_kenda.getText();
        tarif = Integer.parseInt(trf.getText());
    }
    
    public void dataSelect(){
        //deklarasi variabel
        int i = tb_kenda.getSelectedRow();
        
        //uji adakah data di tabel?
        if(i == -1){
            //tidak ada yang terpilih atau dipilih.
            return;
        }
        kd_kenda.setText(""+model.getValueAt(i,0));
        nm_kenda.setText(""+model.getValueAt(i,1));
        trf.setText(""+model.getValueAt(i,2));
    }
    
    public void baru(){
        kd_kendaraan = "";
        nm_kendaraan = "";
        tarif = 0;
               
        kd_kenda.setText(kd_kendaraan);
        nm_kenda.setText(nm_kendaraan);
        trf.setText("");
    }
    
    public void simpanDataKendaraan(){
        //panggil fungsi load data
        loadDataKendaraan();
        
        //uji koneksi dan eksekusi perintah
        try{
            //test koneksi
            Statement stat = (Statement) koneksiDB.getKoneksi().createStatement();
            
            //perintah sql untuk simpan data
            String  sql =   "INSERT INTO jenis_kendaraan(kd_kendaraan, nm_kendaraan, tarif)" + "VALUES('"+ kd_kendaraan +"','"+ nm_kendaraan +"','"+ tarif +"')";
            PreparedStatement p = (PreparedStatement) koneksiDB.getKoneksi().prepareStatement(sql);
            p.executeUpdate();
            
            //ambil data
            getDataKendaraan();
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
    
    public void perbaruiDataKendaraan(){
        //fungsi load data kategori
        loadDataKendaraan();
        
        try{
            //uji koneksi
            Statement stat = (Statement) koneksiDB.getKoneksi().createStatement();
            
            //kirim perintah sql
            String sql = "UPDATE jenis_kendaraan SET nm_kendaraan = '"+ nm_kendaraan +"'," 
                    + " tarif = '"+ tarif +"'" 
                    + " WHERE kd_kendaraan = '"+ kd_kendaraan +"'" ;
            PreparedStatement p =(PreparedStatement)koneksiDB.getKoneksi().prepareStatement(sql);
            p.executeUpdate();
            
            //ambil data
            getDataKendaraan();
            
            //kosongkan data
            baru();
            JOptionPane.showMessageDialog(null, "DATA BERHASIL DIUBAH");
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
